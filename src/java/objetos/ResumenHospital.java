package objetos;

/**
 *
 * @author NachoP
 */
public class ResumenHospital {
    
    private String nombreHospital;
    private int empleadosHospital;
    private int personasPlantilla;
    private int personasDoctor;
    private int sumaSalarial;

    
    
    public String getNombreHospital() {
        return nombreHospital;
    }

    public void setNombreHospital(String nombreHospital) {
        this.nombreHospital = nombreHospital;
    }

    public int getEmpleadosHospital() {
        return empleadosHospital;
    }

    public void setEmpleadosHospital(int empleadosHospital) {
        this.empleadosHospital = empleadosHospital;
    }

    public int getPersonasPlantilla() {
        return personasPlantilla;
    }

    public void setPersonasPlantilla(int personasPlantilla) {
        this.personasPlantilla = personasPlantilla;
    }

    public int getPersonasDoctor() {
        return personasDoctor;
    }

    public void setPersonasDoctor(int personasDoctor) {
        this.personasDoctor = personasDoctor;
    }

    public int getSumaSalarial() {
        return sumaSalarial;
    }

    public void setSumaSalarial(int sumaSalarial) {
        this.sumaSalarial = sumaSalarial;
    }
    
    
}
